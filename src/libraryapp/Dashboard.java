    private void jButtonBSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBSearchActionPerformed
        if(isNumber(jTextFieldBook.getText().trim())){
            jLabelSearch.setText("");
            if(jTextFieldBook.getText().trim().isEmpty()){
                jTextFieldBook.setBorder(javax.swing.BorderFactory.createLineBorder(Color.RED));
                JOptionPane.showMessageDialog(null, "Empty Fields !!", "ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
            BookPojo book = new BookPojo(Integer.parseInt(jTextFieldBook.getText().trim()));
            try{
                if(BookDAO.isBook(book)){
                    jLabelWarning5.setVisible(false);
                    jPanelSearchResults.setVisible(true);
                    jLabelSearch.setText(jTextFieldBook.getText().trim());
                    BookPojo bookDetails = BookDAO.getBookDetailsFromCode(book);
                    DefaultTableModel model = (DefaultTableModel) jTableResults.getModel();
                    model.setRowCount(0);
                    count=0;
                    if(BookDAO.checkBookAvailable(book)){
                        model.addRow(new Object[] {bookDetails.getBname(), bookDetails.getBcode(), bookDetails.getBauthor(), "Available", bookDetails.getBcategory()});
                        count++;
                    }
                    else{
                        model.addRow(new Object[] {bookDetails.getBname(), bookDetails.getBcode(), bookDetails.getBauthor(), "Not Available", bookDetails.getBcategory()});
                        count++;
                    }
                    jLabelTableResults1.setText(count + " Results");
                    return;
                }
                jLabelWarning5.setVisible(true);
            }
            catch(SQLException ex){
                ex.printStackTrace();
            }
            return;
        }
        jLabelSearch.setText("");
        if(jTextFieldBook.getText().trim().isEmpty()){
            jTextFieldBook.setBorder(javax.swing.BorderFactory.createLineBorder(Color.RED));
            JOptionPane.showMessageDialog(null, "Empty Fields !!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        BookPojo book = new BookPojo(jTextFieldBook.getText().trim());
        try{
            if(BookDAO.isBook(book)){
                jPanelSearchResults.setVisible(true);
                jLabelSearch.setText(jTextFieldBook.getText().trim());
                ArrayList <BookPojo> bookList = BookDAO.getBookDetailsFromName(book);
                DefaultTableModel model = (DefaultTableModel) jTableResults.getModel();
                model.setRowCount(0);
                count=0;
                for(BookPojo i: bookList){
                    if(BookDAO.checkBookAvailable(i)){
                        model.addRow(new Object[] {i.getBname(), i.getBcode(), i.getBauthor(), "Available", i.getBcategory()});
                        count++;
                    }
                    else{
                        model.addRow(new Object[] {i.getBname(), i.getBcode(), i.getBauthor(), "Not Available", i.getBcategory()});
                        count++;
                    }
                }
                jLabelTableResults1.setText(count + " Results");
                return;
            }
            jLabelWarning5.setVisible(true);
        }
        catch(SQLException ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButtonBSearchActionPerformed

