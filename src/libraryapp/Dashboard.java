    private void jButtonUpdateProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateProfileActionPerformed
        if(jTextFieldUpdtUsername.getText().trim().isEmpty() || String.valueOf(jPasswordFieldUpdtOPassword.getPassword()).trim().isEmpty() || String.valueOf(jPasswordFieldUpdtNPassword.getPassword()).trim().isEmpty() || String.valueOf(jPasswordFieldUpdtCPassword.getPassword()).trim().isEmpty() || jTextFieldUpdtFName.getText().trim().isEmpty() || jTextFieldUpdtLName.getText().trim().isEmpty()){
            jTextFieldUpdtLName.requestFocus();
            jTextFieldUpdtFName.requestFocus();
            jPasswordFieldUpdtCPassword.requestFocus();
            jPasswordFieldUpdtNPassword.requestFocus();
            jPasswordFieldUpdtOPassword.requestFocus();
            jTextFieldUpdtUsername.requestFocus();
            JOptionPane.showMessageDialog(null, "Empty Fields !!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!(String.valueOf(jPasswordFieldUpdtNPassword.getPassword()).trim().equals(String.valueOf(jPasswordFieldUpdtCPassword.getPassword()).trim()))){
            JOptionPane.showMessageDialog(null, "Password Not Matching !!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            AdminPojo admin = new AdminPojo(jTextFieldUpdtUsername.getText().trim(), String.valueOf(jPasswordFieldUpdtOPassword.getPassword()).trim(), String.valueOf(jPasswordFieldUpdtNPassword.getPassword()).trim(), jTextFieldUpdtFName.getText().trim(), jTextFieldUpdtLName.getText().trim());
            if(AdminDAO.isValidAdmin(admin) && AdminDAO.updateAdmin(admin)){
                th.start();
                JOptionPane.showMessageDialog(null, "Profile Updated !!", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                jTextFieldUpdtUsername.setText("");
                jPasswordFieldUpdtOPassword.setText("");
                jPasswordFieldUpdtNPassword.setText("");
                jPasswordFieldUpdtCPassword.setText("");
                jTextFieldUpdtFName.setText("");
                jTextFieldUpdtLName.setText("");
                jPasswordFieldUpdtCPassword.setBorder(javax.swing.BorderFactory.createLineBorder(Color.BLACK));
                jTextFieldUpdtUsername.requestFocus();
                return;
            }
            JOptionPane.showMessageDialog(null, "Account Not Found!\nCheck Username and Password.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Database Error contact Admin !", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonUpdateProfileActionPerformed

