    private void jButtonSearchIDateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchIDateActionPerformed
        if(jDateIssue.getDate() == null){
            jLabelWarning6.setVisible(true);
            return;
        }
        jLabelDownload.setVisible(true);
        IssueBookPojo date = new IssueBookPojo(jDateIssue.getDate());
        DefaultTableModel model = (DefaultTableModel) jTableIssues.getModel();
        try{
            if(!BookDAO.isBookIssueDateAvailable(date)){
                model.setRowCount(0);
                JOptionPane.showMessageDialog(null, "Date Not Available !!", "ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
            ArrayList <BookPojo> bookList = BookDAO.getIssueListFromDate(date);
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MMM-YY");
            String sDate = sdf.format(jDateIssue.getDate());
            jLabelDate.setText(sDate);
            count=0;
            model.setRowCount(0);
            for(BookPojo i: bookList){
                StudentPojo student = new StudentPojo(i.getSid());
                model.addRow(new Object[] {StudentDAO.getStudentNameFromID(student), i.getSid(), i.getBcode(), sDate});
                count++;
            }
            LabelDownload = "Issue";
            jLabelDateResults.setText(count + " Results");
        }
        catch(SQLException ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButtonSearchIDateActionPerformed

