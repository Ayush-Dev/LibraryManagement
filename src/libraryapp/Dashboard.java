    private void downloadRecord(int location, String fileName, String parameter1, String parameter2, String parameter3, String parameter4) throws IOException, WriteException{
        File file = new File("F:/" + fileName + ".xls");
        if(file.exists()){
            file.delete();
        }
        file.createNewFile();
        WritableWorkbook excel = Workbook.createWorkbook(file);
        WritableSheet sheet = excel.createSheet(fileName, 0);
        Label l1 = new Label(0, location, parameter1);
        Label l2 = new Label(1, location, parameter2);
        Label l3 = new Label(2, location, parameter3);
        Label l4 = new Label(3, location, parameter4);
        sheet.addCell(l1);
        sheet.addCell(l2);
        sheet.addCell(l3);
        sheet.addCell(l4);
        excel.write();
        excel.close();
    }
