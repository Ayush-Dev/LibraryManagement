    private void jButtonCreateProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateProfileActionPerformed
        if(jTextFieldCrtUsername.getText().trim().isEmpty() || jTextFieldCrtFName.getText().trim().isEmpty() || jTextFieldCrtLName.getText().trim().isEmpty() || String.valueOf(jPasswordFieldCrtNPassword.getPassword()).trim().isEmpty() || String.valueOf(jPasswordFieldCrtCPassword.getPassword()).trim().isEmpty()){
            jPasswordFieldCrtCPassword.requestFocus();
            jPasswordFieldCrtNPassword.requestFocus();
            jTextFieldCrtLName.requestFocus();
            jTextFieldCrtFName.requestFocus();
            jTextFieldCrtUsername.requestFocus();
            JOptionPane.showMessageDialog(null, "Empty Fields !!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!jCheckBoxIAgree.isSelected()){
            jLabelWarning2.setVisible(true);
            JOptionPane.showMessageDialog(null, "Agree Terms and Conditions !", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!(String.valueOf(jPasswordFieldCrtNPassword.getPassword()).trim().equals(String.valueOf(jPasswordFieldCrtCPassword.getPassword()).trim()))){
            JOptionPane.showMessageDialog(null, "Password Not Matching !!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        AdminPojo admin = new AdminPojo(jTextFieldCrtUsername.getText().trim(), String.valueOf(jPasswordFieldCrtNPassword.getPassword()).trim(), String.valueOf(jPasswordFieldCrtCPassword.getPassword()).trim(), jTextFieldCrtFName.getText().trim(), jTextFieldCrtLName.getText().trim());
        try{
            if(AdminDAO.isUsernameValid(admin)){
                JOptionPane.showMessageDialog(null, "Username Already Taken !", "ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if(AdminDAO.createAdmin(admin)){
                th.start();
                JOptionPane.showMessageDialog(null, "Admin Account Created !", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                jTextFieldCrtUsername.setText("");
                jTextFieldCrtFName.setText("");
                jTextFieldCrtLName.setText("");
                jPasswordFieldCrtNPassword.setText("");
                jPasswordFieldCrtCPassword.setText("");
                jPasswordFieldCrtCPassword.setBorder(javax.swing.BorderFactory.createLineBorder(Color.BLACK));
                jTextFieldCrtUsername.requestFocus();
            }
        }
        catch(SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error contact Admin !", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonCreateProfileActionPerformed

