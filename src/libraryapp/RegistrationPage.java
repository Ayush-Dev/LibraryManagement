    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed
        if (!jRadioButtonMale.isSelected() && !jRadioButtonFemale.isSelected()) {
            jLabelGenderWarning.setVisible(true);
            if (jTextFieldPhoneNo.getText().trim().isEmpty() || jTextFieldAccsoftID.getText().trim().isEmpty() || jTextFieldLastName.getText().trim().isEmpty() || jTextFieldFirstName.getText().trim().isEmpty()) {
                jTextFieldPhoneNo.requestFocus();
                jTextFieldAccsoftID.requestFocus();
                jTextFieldLastName.requestFocus();
                jTextFieldFirstName.requestFocus();
                jTextFieldHidden.requestFocus();
                String str;
                str = "Empty Fields !!";
                getToolkit().beep();
                JOptionPane.showMessageDialog(null, str, "ACCESS DENIED !!", JOptionPane.ERROR_MESSAGE);
            } else {
                String str;
                str = "Empty Fields !!";
                getToolkit().beep();
                JOptionPane.showMessageDialog(null, str, "ACCESS DENIED !!", JOptionPane.ERROR_MESSAGE);
            }
        } else if (jTextFieldPhoneNo.getText().trim().isEmpty() || jTextFieldAccsoftID.getText().trim().isEmpty() || jTextFieldLastName.getText().trim().isEmpty() || jTextFieldFirstName.getText().trim().isEmpty()) {
            jTextFieldPhoneNo.requestFocus();
            jTextFieldAccsoftID.requestFocus();
            jTextFieldLastName.requestFocus();
            jTextFieldFirstName.requestFocus();
            jTextFieldHidden.requestFocus();
            String str;
            str = "Empty Fields !!";
            JOptionPane.showMessageDialog(null, str, "ACCESS DENIED !!", JOptionPane.ERROR_MESSAGE);
        }
        else {
            String fName = jTextFieldFirstName.getText().trim();
            String lName = jTextFieldLastName.getText().trim();
            String email = jTextFieldEmail.getText().trim();
            String id = jTextFieldAccsoftID.getText().trim();
            String cllg = jComboBoxInstitution.getItemAt(jComboBoxInstitution.getSelectedIndex());
            int sem = Integer.parseInt(jComboBoxSemester.getItemAt(jComboBoxSemester.getSelectedIndex()));
            String sec = jComboBoxSection.getItemAt(jComboBoxSection.getSelectedIndex());
            long pno = Long.parseLong(jTextFieldPhoneNo.getText().trim());
            String branch = jComboBoxBranch.getItemAt(jComboBoxBranch.getSelectedIndex());
            String gen;
            if (jRadioButtonMale.isSelected()) {
                gen = "Male";
            } else {
                gen = "Female";
            }
            try {
                StudentPojo studentDetails = new StudentPojo(fName, lName, email, id, cllg, sem, sec, pno, gen, branch);
                if (addStudent(studentDetails)) {
//                    Voice voice;
//                    VoiceManager vm = VoiceManager.getInstance();
//                    voice = vm.getVoice(VOICENAME);
//                    voice.allocate();
//                    voice.speak("Account Successfully Created");
                    JOptionPane.showMessageDialog(null, "Account Successfully Created", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    jTextFieldFirstName.setText("");
                    jTextFieldLastName.setText("");
                    jTextFieldEmail.setText("");
                    jTextFieldAccsoftID.setText("");
                    jTextFieldPhoneNo.setText("");
                    jTextFieldHidden.requestFocus();
                }
            } catch (SQLException ex) {
                if (ex.getErrorCode() == 1) {
                    JOptionPane.showMessageDialog(null, "Accsoft ID or Mobile Number already exists !!");
                }
                else {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButtonRegisterActionPerformed

