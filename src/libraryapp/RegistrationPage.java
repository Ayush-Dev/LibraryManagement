    private void jTextFieldAccsoftIDKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldAccsoftIDKeyTyped
        char c = evt.getKeyChar();
        String id;
        id = jTextFieldAccsoftID.getText().trim();
        if (!(Character.isDigit(c) || c == KeyEvent.VK_BACKSPACE || c == KeyEvent.VK_DELETE) || id.length() > 10) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_jTextFieldAccsoftIDKeyTyped

    private void jTextFieldPhoneNoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPhoneNoKeyTyped
        char c;
        c = evt.getKeyChar();
        String pno;
        pno = jTextFieldPhoneNo.getText().trim();
        if (!(Character.isDigit(c) || c == KeyEvent.VK_BACKSPACE || c == KeyEvent.VK_DELETE) || pno.length() > 9) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_jTextFieldPhoneNoKeyTyped

    private void jTextFieldFirstNameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldFirstNameKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_SPACE) {
            jTextFieldLastName.requestFocus();
        }
    }//GEN-LAST:event_jTextFieldFirstNameKeyPressed

