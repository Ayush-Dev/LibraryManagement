    private void jButtonAddBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddBookActionPerformed
        if (jTextFieldBName.getText().trim().isEmpty() || jTextFieldBAuthor.getText().trim().isEmpty() || jTextFieldBCost.getText().trim().isEmpty() || jTextFieldBPublisher.getText().trim().isEmpty() || jTextFieldBCode.getText().trim().isEmpty() || jTextFieldBCount.getText().trim().isEmpty()) {
            jTextFieldBName.requestFocus();
            jTextFieldBAuthor.requestFocus();
            jTextFieldBCost.requestFocus();
            jTextFieldBPublisher.requestFocus();
            jTextFieldBCode.requestFocus();
            jTextFieldBCount.requestFocus();
            jTextFieldHidden.requestFocus();
            String str;
            str = "Empty Fields !!";
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, str, "ACCESS DENIED !!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String bname = jTextFieldBName.getText().trim();
        String bauthor = jTextFieldBAuthor.getText().trim();
        int bcost = Integer.parseInt(jTextFieldBCost.getText().trim());
        int bcount = Integer.parseInt(jTextFieldBCount.getText().trim());
        String bcategory = jComboBoxBCategory.getItemAt(jComboBoxBCategory.getSelectedIndex());
        int bcode = Integer.parseInt(jTextFieldBCode.getText().trim());
        String bpublisher = jTextFieldBPublisher.getText().trim();
        try {
            int max = bcode + bcount;
            while (bcode < max) {
                BookPojo bookDetails = new BookPojo(bname, bauthor, bcost, bcategory, bpublisher, bcode);
                if (BookDAO.addBook(bookDetails)) {
                    bcode++;
                }
            }
            Dashboard d = new Dashboard();
            d.setLables();
            JOptionPane.showMessageDialog(null, "Books Saved Successfully !", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
            jTextFieldBName.setText("");
            jTextFieldBAuthor.setText("");
            jTextFieldBCost.setText("");
            jTextFieldBCount.setText("");
            jTextFieldBPublisher.setText("");
            jTextFieldBCode.setText("");
        }
        catch (SQLException ex) {
            if (ex.getErrorCode() == 1) {
                JOptionPane.showMessageDialog(null, "Book Code Already Allotted !!", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            else {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonAddBookActionPerformed

