    private void jTextFieldBCountKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldBCountKeyTyped
        char c;
        c = evt.getKeyChar();
        if (!(Character.isDigit(c) || c == KeyEvent.VK_BACKSPACE || c == KeyEvent.VK_DELETE)) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_jTextFieldBCountKeyTyped

